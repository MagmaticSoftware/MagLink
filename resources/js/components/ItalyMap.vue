export interface ItalyRegion {
    id: string;
    istat: number;
    name: string;
    value?: number;
}

interface Props {
    regions?: ItalyRegion[];
    selectedRegion?: string | null;
    hoveredRegion?: string | null;
}

const props = withDefaults(defineProps<Props>(), {
    regions: () => [],
    selectedRegion: null,
    hoveredRegion: null,
});

const emit = defineEmits<{
    'region-click': [regionId: string];
    'region-hover': [regionId: string | null];
}>();

const getRegionValue = (regionId: string): number | undefined => {
    return props.regions.find((r) => r.id === regionId)?.value;
};

const getMaxValue = (): number => {
    const values = props.regions.map((r) => r.value ?? 0);
    return values.length > 0 ? Math.max(...values) : 0;
};

const getRegionFill = (regionId: string): string => {
    if (props.selectedRegion === regionId) return '#1d4ed8';
    if (props.hoveredRegion === regionId) return '#3b82f6';
    const value = getRegionValue(regionId);
    const max = getMaxValue();
    if (value !== undefined && max > 0) {
        const intensity = Math.round((value / max) * 200);
        return `rgb(${255 - intensity}, ${255 - intensity}, 255)`;
    }
    return '#d1d5db';
};
